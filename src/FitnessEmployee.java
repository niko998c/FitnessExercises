public class FitnessEmployee {
    String name;
    String cpr;
    String empType;
    int hours;

    public FitnessEmployee() {
    }

    public FitnessEmployee(String name, String cpr, String empType, int hours) {
        this.name = name;
        this.cpr = cpr;
        this.empType = empType;
        this.hours = hours;
    }
}
